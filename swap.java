class swap
{
	public static void main(String arga[])
	{
		int a=4,b=5,c;
		System.out.println("Before swapping numbers are");
		System.out.println("a="+a);
		System.out.println("b="+b);
		c=a;
		a=b;
		b=c;
		System.out.println("After swapping numbers are");
		System.out.println("a="+a);
		System.out.println("b="+b);
	}
}
